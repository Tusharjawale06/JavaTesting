package dataType;

public class DataType {

	public static void main(String[] args) {
		
		byte b = 127;
		short s = -32768;
		int i = -2147483648;
		
		
		float f = 3.1415927f;
		double d = 3.141592653589793;
		
		char ca = 'a';
		char cn = '1';
		char csc = '#';
		
		boolean truee = true;
		boolean falsee = false;		
		
		String str = "ARC Technologies";
		
		String HOME_PAGE_HEADING = "Welcome To ARC";
		
		System.out.println("Byte Value is :"+b);
		System.out.println("Short Value is :"+s);
		System.out.println("Integer Value is :"+i);
		System.out.println("Float Value is :"+f);
		System.out.println("Decimal Value is :"+d);
		System.out.println("Character Alphabet Value is :"+ca);
		System.out.println("Character Number Value is :"+cn);
		System.out.println("Character Special Value is :"+csc);
		System.out.println("True Value is :"+truee);
		System.out.println("False Value is :"+falsee);
		System.out.println("String Value is :"+str);
		System.out.println("String Constant Value is :"+HOME_PAGE_HEADING);
		
	    
		
	}

}
